export const getSrsKickOffsets = (
  pieceType: PieceType,
  fromRotation: number,
  toRotation: number
): KickOffset[] => {
  if (pieceType === "O") {
    return [{ x: 0, y: 0 }];
  }
  const key: RotationPair = `${fromRotation}>${toRotation}`;
  return pieceType === "I" ? I_KICKS[key] ?? [{ x: 0, y: 0 }] : JLSTZ_KICKS[key] ?? [{ x: 0, y: 0 }];
};
